-- ① 부류 테이블 (대분류)
CREATE TABLE IF NOT EXISTS public.kamis_categories (
  category_code text PRIMARY KEY,
  category_name text NOT NULL,
  sort_order int NOT NULL DEFAULT 0
);

-- ② 품목 테이블 (소분류, category_code로 부류 연결)
CREATE TABLE IF NOT EXISTS public.kamis_items (
  item_code text PRIMARY KEY,
  category_code text NOT NULL REFERENCES public.kamis_categories(category_code),
  item_name text NOT NULL,
  sort_order int NOT NULL DEFAULT 0
);
CREATE INDEX IF NOT EXISTS idx_kamis_items_category ON public.kamis_items(category_code);

